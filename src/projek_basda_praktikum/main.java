    private void view_search_db(String key) {
        Object[] baris = {"ID", "Nama", "Harga", "Stock"};
        tabmode = new DefaultTableModel(null, baris);
        tabel_barangkasir.setModel(tabmode);
        try {
            hasil = aa.cariData(key);
            while (hasil.next()) {
                String data[] = {hasil.getString("id_barang"), hasil.getString("nama"), hasil.getString("harga"), hasil.getString("stock")};
                tabmode.addRow(data);
            }
        } catch (Exception e) {
        }
    }

