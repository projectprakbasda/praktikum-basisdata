    private void tabel_barangkasirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel_barangkasirMouseClicked
        try {
            int row = tabel_barangkasir.rowAtPoint(evt.getPoint());

            String id_barang = tabel_barangkasir.getValueAt(row, 0).toString();
            String nama = tabel_barangkasir.getValueAt(row, 1).toString();
            String harga = tabel_barangkasir.getValueAt(row, 2).toString();
            String stock = tabel_barangkasir.getValueAt(row, 3).toString();

            txt_idbrg.setText(String.valueOf(id_barang));
            txt_namabrg.setText(String.valueOf(nama));
            txt_harga.setText(String.valueOf(harga));
            txt_stok.setText(String.valueOf(stock));

        } catch (Exception e) {

        }
    }//GEN-LAST:event_tabel_barangkasirMouseClicked

