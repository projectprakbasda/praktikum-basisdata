    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        if (txt_idbrg.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Maaf, ID Barang belum diisi !");
            txt_idbrg.requestFocus();
        } else if (txt_namabrg.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Maaf Nama Barang belum diisi !");
            txt_namabrg.requestFocus();
        } else if (txt_harga.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Maaf Harga Barang belum diisi !");
            txt_harga.requestFocus();
        } else if (txt_stok.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Maaf Banyak Stok belum diisi !");
            txt_stok.requestFocus();
        } else {
            String nama = "", harga = "";
            int id_barang, stock;
            try {

                id_barang = Integer.parseInt(txt_idbrg.getText());
                stock = Integer.parseInt(txt_stok.getText());
                aa.setID(id_barang);
                aa.setNama(txt_namabrg.getText());
                aa.setHarga(txt_harga.getText());
                aa.setStock(stock);
                aa.simpanData(aa.getID(), aa.getNama(), aa.getHarga(), aa.getStock());

                JOptionPane.showMessageDialog(null, "Data berhasil tersimpan", "informasi", JOptionPane.INFORMATION_MESSAGE);
                tampil_database();
                reset_text();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data gagal tersimpan", "informasi", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btn_simpanActionPerformed

