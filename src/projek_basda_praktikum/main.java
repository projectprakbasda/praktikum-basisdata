    private void tampil_database() {

        Object[] baris = {"ID", "Nama", "Harga", "Stock",};
        tabmode = new DefaultTableModel(null, baris);
        tabel_barangkasir.setModel(tabmode);

        try {
            hasil = aa.tampilData();
            while (hasil.next()) {
                aa.setID(hasil.getInt("id_barang"));
                aa.setNama(hasil.getString("nama"));
                aa.setHarga(hasil.getString("harga"));
                aa.setStock(hasil.getInt("stock"));
                Object[] data = {aa.getID(), aa.getNama(), aa.getHarga(), aa.getStock()};
                tabmode.addRow(data);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

